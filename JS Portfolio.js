function setLanguage(lang) {
  const t = translations[lang];

  // Sécurisation des éléments avant modification
  const safeSet = (id, content, isHTML = false) => {
    const el = document.getElementById(id);
    if (el) {
      if (isHTML) {
        el.innerHTML = content;
      } else {
        el.textContent = content;
      }
    }
  };

  safeSet("greeting-name", t.greetingName, true);
  safeSet("greeting-role", t.greetingRole);
  safeSet("greeting-description", t.greetingDescription);
  safeSet("profile-title", t.profileTitle);
  safeSet("profile-text", t.profileText);
  safeSet("expertise-title", t.expertiseTitle);
  safeSet("expertise-text", t.expertiseText);
  safeSet("experience-title", t.experienceTitle);
  safeSet("skills-title", t.skillsTitle);

  // Expériences
  const experienceContainer = document.getElementById("experience-list");
  if (experienceContainer) {
    experienceContainer.innerHTML = t.experienceDetails
      .map(exp => `
        <div class="accordion-item">
          <button class="accordion-toggle">${exp.title}</button>
          <div class="accordion-content" style="display: none;">
            <p><i><strong>${exp.company}</strong></i></p>
            <ul>
              ${exp.missions.map(m => `<li>${m}</li>`).join("")}
            </ul>
          </div>
        </div>
      `)
      .join("");
  }

  // Réactivation des accordéons
  document.querySelectorAll('.accordion-toggle').forEach(button => {
    button.addEventListener('click', () => {
      const content = button.nextElementSibling;
      const isOpen = content.style.display === 'block';
      document.querySelectorAll('.accordion-content').forEach(c => c.style.display = 'none');
      content.style.display = isOpen ? 'none' : 'block';
    });
  });

  // Compétences
  const skillsContainer = document.getElementById("skills-list");
  if (skillsContainer) {
    skillsContainer.innerHTML = t.skillsList
      .map(skill => `<li>${skill}</li>`)
      .join("");
  }
}

// Initialisation après chargement du DOM
document.addEventListener("DOMContentLoaded", () => {
  setLanguage("fr");
});
